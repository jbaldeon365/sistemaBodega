    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("Lote");
        modeloTabla.addColumn("Tipo Producto");
        modeloTabla.addColumn("Nombre Producto");
        modeloTabla.addColumn("Fecha Vencimiento");
        modeloTabla.addColumn("Cantidad");
        Object[] rowdataBusqueda = new Object[5];
        
        if (cbTipoProducto.getSelectedIndex() == 0) {
            listInventario = inventarioController.consultaInventario();
        } else {
            listInventario = inventarioController.buscarInventario(cbTipoProducto.getSelectedItem().toString(), cbNombreProducto.getSelectedItem().toString());
        }
        
        for (int i = 0; i < listInventario.size(); i++) {
            rowdataBusqueda[0] = listInventario.get(i).getLoteProducto();
            rowdataBusqueda[1] = listInventario.get(i).getTipoProducto();
            rowdataBusqueda[2] = listInventario.get(i).getNombreProducto();
            rowdataBusqueda[3] = listInventario.get(i).getFechaVencimientoProducto();
            rowdataBusqueda[4] = listInventario.get(i).getCantidadProducto();
            modeloTabla.addRow(rowdataBusqueda);
        }
        tableInventario.setModel(modeloTabla);
        
        int cantidadproductototal=0;
        for (int i = 0; i < listInventario.size(); i++) {
            cantidadproductototal += listInventario.get(i).getCantidadProducto();
        }
        
        txtCantidadProductos.setText(Integer.toString(cantidadproductototal));
        txtValorTotal.setText(Float.toString(inventarioController.calculoValorTotal(cbTipoProducto.getSelectedItem().toString(), cbNombreProducto.getSelectedItem().toString())));
    }//GEN-LAST:event_btnBuscarActionPerformed

