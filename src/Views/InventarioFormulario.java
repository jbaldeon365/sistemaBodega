    public InventarioFormulario() {
        initComponents();
        DefaultTableModel modelo = (DefaultTableModel) tableInventario.getModel();
        DefaultComboBoxModel tipoProductoModel = new DefaultComboBoxModel();
        
        listInventario = inventarioController.consultaInventario();
        listaTipoProducto = inventarioController.rellenarTipoProducto();
        Object[] rowdata = new Object[5];
        
        for (int i = 0; i < listInventario.size(); i++) {
            rowdata[0] = listInventario.get(i).getLoteProducto();
            rowdata[1] = listInventario.get(i).getTipoProducto();
            rowdata[2] = listInventario.get(i).getNombreProducto();
            rowdata[3] = listInventario.get(i).getFechaVencimientoProducto();
            rowdata[4] = listInventario.get(i).getCantidadProducto();
            modelo.addRow(rowdata);
        }
        tableInventario.setModel(modelo);
        
        int cantidadproductototal=0;
        for (int i = 0; i < listInventario.size(); i++) {
            cantidadproductototal += listInventario.get(i).getCantidadProducto();
        }
        for (int i = 0; i < listaTipoProducto.size(); i++) {
            tipoProductoModel.addElement(listaTipoProducto.get(i));
        }
        txtCantidadProductos.setText(Integer.toString(cantidadproductototal));
        txtValorTotal.setText(Float.toString(inventarioController.calculoValorTotal("", "")));
        cbTipoProducto.setModel(tipoProductoModel);
    }

